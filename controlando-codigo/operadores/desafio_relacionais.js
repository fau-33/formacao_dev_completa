let a = 5;
let b = 7;

// 5 expressões em que o resultado seja verdadeiro(true)
console.log(a < b);
console.log(a <= b);
console.log(a != b);
console.log(a !== b);
console.log(b > a);

// 5 expressões em que o resultado seja falso(false)
console.log(a > b);
console.log(a >= b);
console.log(a == b);
console.log(a === b);
console.log(b < a);
